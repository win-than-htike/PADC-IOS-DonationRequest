import UIKit
class CellRegisterUtil {
    static func cellRegister(nibName: String, collectionView: UICollectionView){
        let nib = UINib(nibName: nibName, bundle: nil)
        collectionView.register(nib, forCellWithReuseIdentifier: nibName)
    }
}
